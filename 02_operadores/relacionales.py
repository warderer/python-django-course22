# Operadores relacionales


## Operador ==
print(5 == 6)


## Operador !=
print(5 != 6)


## Operador >
print(5 > 6)


## Operador <
print(5 < 6)


## Operador >=
print(6 >= 6)


## Operador <=
print(5 <= 6)


print('=======')

print('Hola' == 'Hola')

print([1, 2] == [1, 2])