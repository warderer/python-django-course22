# Operadores

x = 10
y = 3

# Operador +
print(x + y)
# print("2" + "3")  # Concatenación
# print([1, 2] + [3, 4])


# Operado -
print(x - y)


# Operado *
print(x * y)


# Operado /
print(x / y)


# Operado %
print(x % y) # 1


# Operado **
print(x ** y)

# Operado //
print(x // y) # 3

# Prioridades de operación
# ()
# **
# *  /  //
# + -